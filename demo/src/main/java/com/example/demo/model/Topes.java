package com.example.demo.model;

public class Topes {
    private int idTopes;
    private int idSabana;
    private int idTipoTopes;
    private int topes;

    public int getIdTopes() {
        return idTopes;
    }

    public void setIdTopes(int idTopes) {
        this.idTopes = idTopes;
    }

    public int getIdSabana() {
        return idSabana;
    }

    public void setIdSabana(int idSabana) {
        this.idSabana = idSabana;
    }

    public int getIdTipoTopes() {
        return idTipoTopes;
    }

    public void setIdTipoTopes(int idTipoTopes) {
        this.idTipoTopes = idTipoTopes;
    }

    public int getTopes() {
        return topes;
    }

    public void setTopes(int topes) {
        this.topes = topes;
    }

}
